char *ft_check_arg(char *arg1)
{
	int i;
	int	j;
	char	ptr[15];

	i = 0;
	j = 0;
	while (arg1[i] != '\0')
	{
		if (arg1[i] >= '0' && arg1[i] <= '9')
		{
			ptr[j] = arg1[i];
			j++;
		}
		i++;
	}
	return ptr;
}
